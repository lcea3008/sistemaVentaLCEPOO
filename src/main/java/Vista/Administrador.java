    private void btnGuardarProActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarProActionPerformed
        // TODO add your handling code here:
        try {
            int nn = comb.getSelectedIndex();
            //cod.setText(String.valueOf(nn));
            if (nn == 0) {
                JOptionPane.showMessageDialog(null, "Debe selecionar una categoria");
            } else {
                if ((nom1.getText().equals("")) || (pre1.getText().equals(""))
                        || (sto1.getText().equals(""))) {

                    JOptionPane.showMessageDialog(null, "Debe llenar todos los espacios");

                } else {
                    SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd");

                    p1.setCodigo_catategoria(nn);

                    p1.setNombre(nom1.getText());

                    String pa = pre1.getText();
                    double prec = Double.parseDouble(pa);
                    p1.setPrecio((float) prec);

                    String ss = sto1.getText();
                    int stoc = Integer.parseInt(ss);
                    p1.setStock(stoc);
                    p1.setProveedor(comboPro.getSelectedIndex());
                    Date venc = dateFormat.parse(dateFormat.format(fvenc1.getDate()));
                    p1.setFechaVencimiento(new java.sql.Date(venc.getTime()));

                    mp.insertarProductos(p1);

                    JOptionPane.showMessageDialog(null, "Registro exitoso ");
                    //limpiar();
                    admi.mostarProductos(TableProductos);
                }
            }
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, "Intente de nuevo" + ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Administrador.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnGuardarProActionPerformed

