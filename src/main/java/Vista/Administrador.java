    public void actualizar() {

        for (int i = 0; i < TableVenta.getRowCount(); i++) {
            int idactual = Integer.parseInt(TableVenta.getValueAt(i, 0).toString());
            int canti = Integer.parseInt(TableVenta.getValueAt(i, 2).toString());
            try {
                Conexion con = new Conexion();
                Connection lce = con.getConectar1();
                PreparedStatement ps;
                String sql = "UPDATE productos SET stock=stock-? WHERE idProducto=?";
                ps = lce.prepareStatement(sql);
                ps.setInt(1, canti);
                ps.setInt(2, idactual);
                ps.execute();

            } catch (SQLException ex) {
                System.out.println("Error al modificar el stock " + ex);
            }
        }

    }

