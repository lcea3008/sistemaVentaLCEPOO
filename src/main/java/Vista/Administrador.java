    private void totalizar() {
        double subt = 0;
        double p = 0;
        if (TableVenta.getRowCount() > 0) {
            for (int i = 0; i < TableVenta.getRowCount(); i++) {
                p = Double.parseDouble(TableVenta.getValueAt(i, 4).toString());
                subt += p;
            }
            double igv = 0.18 * subt;
            double total = subt + igv;
            String formaTotal = String.format("%.2f", total);
            LabelTotal.setText(formaTotal);
        }
    }

    public String calcularPrecio() {
        float precio = Float.parseFloat(txtPrecio.getText());
        int cantidad = Integer.parseInt(txtCantidadVenta.getText());
        float prec = precio * cantidad;
        String fomatoprecio = String.format("%.2f", prec);
        return fomatoprecio;
    }

