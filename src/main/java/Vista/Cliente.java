            DetalleVenta ps = new DetalleVenta();
            ps.setVentaid(idv);
            ps.setIdProducto(Integer.parseInt(tabla2.getValueAt(i, 0).toString()));
            ps.setNombre_producto(tabla2.getValueAt(i, 1).toString());
            ps.setPrecio_u(Float.parseFloat(tabla2.getValueAt(i, 2).toString()));
            ps.setCantidad(Integer.parseInt(tabla2.getValueAt(i, 3).toString()));
            ps.setSubtotal(Float.parseFloat(tabla2.getValueAt(i, 4).toString()));
