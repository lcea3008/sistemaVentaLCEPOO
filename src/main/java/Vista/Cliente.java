    private int buscarProductoEnSegundaTabla(DefaultTableModel modelo, String idProducto) {
        for (int i = 0; i < modelo.getRowCount(); i++) {
            if (modelo.getValueAt(i, 0).equals(idProducto)) {
                return i; // Retorna el Ã­ndice si el producto ya existe
            }
        }
        return -1; // Retorna -1 si el producto no existe
    }

